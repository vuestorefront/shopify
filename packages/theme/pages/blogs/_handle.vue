import {
  SfHeading,
  SfButton,
  SfList,
  SfIcon,
  SfMenuItem,
  SfProductCard,
  SfPagination,
  SfAccordion,
  SfComponentSelect,
  SfBreadcrumbs,
  SfProductCardHorizontal,
  SfSelect
} from '@storefront-ui/vue';
import { useRoute } from '@nuxtjs/composition-api';
import { onSSR } from '@vue-storefront/core';
import { useContent } from '@vue-storefront/shopify';
import { ContentType } from '@vue-storefront/shopify/src/types/ContentType';
export default {
  name: 'Category',
  components: {
    SfHeading,
    SfButton,
    SfIcon,
    SfList,
    SfProductCard,
    SfPagination,
    SfMenuItem,
    SfAccordion,
    SfComponentSelect,
    SfBreadcrumbs,
    SfSelect,
    SfProductCardHorizontal
  },
  setup() {
    const route = useRoute();
    const { search: getBlogs, content: blogs } = useContent('blogs');
    const { search: searchBlog } = useContent('blog');
    const { search: searchArticles, content: articles } = useContent('articles')

    onSSR(async () => {
      await getBlogs({ contentType: ContentType.Blog });
      let handle = route?.value?.params?.handle

      if (route.value.params.handle) {
        await searchBlog({
          contentType: ContentType.Blog,
          handle
        });
      } else if (blogs) {
        handle = blogs?.value?.[0]?.handle
        await searchBlog({
          contentType: ContentType.Blog,
          id: blogs?.value?.[0]?.id
        });
      }

      await searchArticles({ contentType: ContentType.Article, query: `blog_title:${handle}` })
    });


    return {
      blogs,
      articles,
      totalPosts: 0,
      currentPage: 1,
      sortBy: 'Latest',
      isGridView: true,
      category: 'Blogs',
      sortByOptions: [
        {
          value: 'Latest',
          label: 'Latest'
        }
      ],
      sidebarAccordion: [
        {
          header: 'Blogs'
        }
      ],
      showOnPage: ['20', '40', '60'],
      breadcrumbs: [
        {
          text: 'Home',
          link: '/'
        },
        {
          text: 'Blogs',
          link: '/blogs'
        }
      ]
    };
  }
};
</script>
